/* ─────────────── Helper Functions ─────────────── */

export function getRegion(province: string): string {
  return PROVINCE_REGION_MAP[province] || "ไม่ระบุ";
}

export function getCompanyStatusBadgeColor(status: string): string {
  switch (status) {
    case "รายเดือน":
      return "green";
    case "รายเดือน / วางมือ":
      return "yellow";
    case "รายเดือน / จ่ายรายปี":
      return "blue";
    case "รายเดือน / เดือนสุดท้าย":
      return "orange";
    case "ยกเลิกทำ":
      return "red";
    default:
      return "gray";
  }
}
